public class Person{
    private String name;
    private int age;

    public person(String name, int age){
        this.name = name;
        this.age = age;
    }
    //getters and setters
    public void getName(){
        return name;
    }
    public void setName(String value){
        name = value;
    }
    public void getAge(){
        return age;
    }
    public void setAge(int setAge){
        age = setAge;
    }
}